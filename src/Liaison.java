
public class Liaison {
	
	Sortie entree;
	Sortie sortie;
	
	public Liaison(Sortie entree, Sortie sortie) {
		this.entree = entree;
		this.sortie = sortie;
	}

	public Sortie getEntree() {
		return entree;
	}

	public void setEntree(Sortie entree) {
		this.entree = entree;
	}

	public Sortie getSortie() {
		return sortie;
	}

	public void setSortie(Sortie sortie) {
		this.sortie = sortie;
	}
	
	

}
